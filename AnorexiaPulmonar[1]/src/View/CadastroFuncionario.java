    private void b_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_cadastrarActionPerformed

        try {
            // recebendo e validando dados da interface gráfica.
            String nome = "";
            int idade = 0;
            String cargo = "";
            String CPF = "";
            int PIS = 0;

            if (this.c_nomeFunc.getText().length() < 2) {
                throw new Mensagens("Nome deve conter ao menos 2 caracteres.");
            } else {
                nome = this.c_nomeFunc.getText();
            }

            if (this.c_idadeFunc.getText().length() <= 0) {
                throw new Mensagens("Idade deve ser n�mero e maior que zero.");
            } else {
                idade = Integer.parseInt(this.c_idadeFunc.getText());
            }
            
            if (this.c_cargoFunc.getText().length() < 2) {
                throw new Mensagens("Cargo deve conter ao menos 2 caracteres.");
            } else {
                cargo = this.c_cargoFunc.getText();
            }

            if (this.c_CPFFunc.getText().length() != 14) {
                throw new Mensagens("CPF deve conter 11 digitos.");
            } else {
                CPF = this.c_CPFFunc.getText();
            }

            if (this.c_PISFunc.getText().length() <6) {
                throw new Mensagens("PIS deve conter mais de 6 digitos.");
            } else {
                PIS = Integer.parseInt(this.c_PISFunc.getText());
            }
            
            // envia os dados para o Controlador cadastrar
            if (this.objfunc.InsertFuncionarioBD(cargo, CPF, PIS, nome, idade)) {
                JOptionPane.showMessageDialog(rootPane, "Funcionario Cadastrado com Sucesso!");

                // limpa campos da interface
                this.c_nomeFunc.setText("");
                this.c_idadeFunc.setText("");
                this.c_cargoFunc.setText("");
                this.c_CPFFunc.setText("");
                this.c_PISFunc.setText("");

            }

            System.out.println(this.objfunc.getMinhaListaFunc().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um numero.");
        }


    }//GEN-LAST:event_b_cadastrarActionPerformed

